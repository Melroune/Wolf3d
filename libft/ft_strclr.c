#include "libft.h"

void	ft_strclr(char *s)
{
	int c;
	int	len;

	if (s == NULL)
		return ;
	c = 0;
	len = ft_strlen(s);
	while (c < len)
	{
		s[c] = 0;
		c++;
	}
	s[c] = '\0';
}
